#              OPERATIONAL REQUIREMENT (code-reviewer finding, 2026-07-22
#              review 1/3): every _table_exists() guard below is a
#              check-then-act read, not an atomic compare-and-create — safe
#              only when the 3 servers run `alembic upgrade head` one at a
#              time (as intended: each is a one-off deploy step, not a
#              long-running concurrent process). Two servers migrating this
#              shared DB at the exact same moment could both pass the
#              existence check before either commits its create_table, then
#              both attempt to create the same table — this migration does
#              NOT guard against that race. Deploy runbooks must serialise
#              the 3 servers' first migration run.

def _table_exists(name: str) -> bool:
    """Whether ``name`` already exists on the DB this migration is running against.

    Used to guard every create/drop below so this migration is safe to replay
    (see module docstring) — a 2nd/3rd server's independent, from-base replay
    of this same revision must not error on tables an earlier server already
    created.
    """
    inspector = sa.inspect(op.get_bind())
    return inspector.has_table(name)


